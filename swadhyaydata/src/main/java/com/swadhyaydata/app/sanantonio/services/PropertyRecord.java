package com.swadhyaydata.app.sanantonio.services;

public class PropertyRecord {
    private String propertyId;
    private String geographicId;
    private String type;
    private String propertyAddress;
    private String legalDescription;
    private String ownerName;
    private String doingBusinessAs;
    private String appraisedValue;
	public String getPropertyId() {
		return propertyId;
	}
	public void setPropertyId(String propertyId) {
		this.propertyId = propertyId;
	}
	public String getGeographicId() {
		return geographicId;
	}
	public void setGeographicId(String geographicId) {
		this.geographicId = geographicId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPropertyAddress() {
		return propertyAddress;
	}
	public void setPropertyAddress(String propertyAddress) {
		this.propertyAddress = propertyAddress;
	}
	public String getLegalDescription() {
		return legalDescription;
	}
	public void setLegalDescription(String legalDescription) {
		this.legalDescription = legalDescription;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public String getDoingBusinessAs() {
		return doingBusinessAs;
	}
	public void setDoingBusinessAs(String doingBusinessAs) {
		this.doingBusinessAs = doingBusinessAs;
	}
	public String getAppraisedValue() {
		return appraisedValue;
	}
	public void setAppraisedValue(String appraisedValue) {
		this.appraisedValue = appraisedValue;
	}

    
}
